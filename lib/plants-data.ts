export function plantImageUrl(plant: Plant): string {
  const genus = plant.nameLatin.split(' ')[0].toLowerCase();
  return `https://source.unsplash.com/400x300/?${encodeURIComponent(genus)},plant&sig=${plant.id}`;
}

