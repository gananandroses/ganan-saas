// Coarse-grained Israeli geographic regions used by /schedule/plan when
// the gardener wants to combine customers from neighbouring cities into
// a single planning bucket. Driving between two cities in the same
// region is short enough that they belong on the same day's route.
//
// Membership is best-effort, not exhaustive. Cities that aren't on the
// map fall back to a "אחר" region. The match is case-insensitive and
// tolerant to whitespace.

